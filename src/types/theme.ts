/**
 * Shape of a named custom theme registered in the provider.
 */
export type UiThemeDefinition = {
  colors?: UiThemeColorTokens;
};

/**
 * Collection of named themes available to the provider.
 */
export type UiThemeRegistry = Record<string, UiThemeDefinition>;

/**
 * Props accepted by the library theme provider.
 */
export type UiThemeProviderProps = PropsWithChildren<
  Partial<{
    /**
     * Adds custom inline styles to the wrapper element.
     */
    style: CSSProperties;
    /**
     * Adds a custom class name to the wrapper element.
     */
    className: string;
    /**
     * Selects the active theme name or follows the system preference.
     * @default 'light'
     */
    theme: UiThemeName;
    /**
     * Registers additional named themes that can be selected through `theme`.
     */
    themes: UiThemeRegistry;
    /**
     * Applies token overrides on top of the resolved theme.
     */
    tokens: UiThemeColorTokens;
  }>
>;

/**
 * Context value exposed by the library theme provider.
 */
export type UiThemeContextValue = {
  resolvedTheme: UiBuiltinThemeName | string;
  theme: UiThemeName;
  tokens: UiThemeColorTokens;
};
