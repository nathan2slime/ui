import type { CSSProperties, PropsWithChildren } from 'react';

/**
 * Built-in color theme names shipped by the library.
 */
export type UiBuiltinThemeName = 'dark' | 'light';

/**
 * Special theme mode that follows the user's operating system preference.
 */
export type UiSystemThemeName = 'system';

/**
 * Theme value accepted by the provider.
 *
 * Custom theme names can be registered through the `themes` prop.
 */
export type UiThemeName = string | UiSystemThemeName;

